            $connobj->query($sql);
        }
        
        function update_post()
        {
            $connobj = new Connection;
            
            $title = $_POST["title"];
            $content = $_POST["content"];
            $date = $_POST["date"];
            $userid = $_SESSION["userid"];
            $journal_id = $_POST['journal_id'];
            if (isset($journal_id))
            {
                $sql = "UPDATE journal 
                            SET title = '$title', 
                                content = '$content', 
                                date = STR_TO_DATE('$date', '%m/%d/%Y'), 
                                user_id = '$userid'
                            WHERE journal_id = '$journal_id';";
            }
            $connobj->query($sql);
        }
